C=int(input("Enter temperature in celsius: "))
F=int(input("Enter temperature in Farenheit: "))
def convert_cel_to_far(C):
    F1=C * 9 / 5 + 32
    return F1
def convert_far_to_cel(F):
    C2=(F-32) * 5/9
    return C2


print(f"37 degrees Celsius = {convert_cel_to_far(C):.2f} degrees F")
print(f"72 degrees Farenheit = {convert_far_to_cel(F):.2f} degrees C")


